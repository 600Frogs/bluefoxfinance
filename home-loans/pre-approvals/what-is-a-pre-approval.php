    <h1>What is a pre approval?</h1>
    <p>What is a pre approval? A pre approval is a document issued by a bank or lender letting you now that they have assessed your situation and are likely to give you a loan at a later date.  Before looking for a house, many borrowers look at getting a pre-approval in place so they can enter a contract with a bit more confidence.  To get a pre approval, you will generally be required to go through a full loan application with the lender of choice, but without an actual purchase property; thus giving the <a href="<?php echo $config['URL'] . 'useful-information/glossary.php/#lender' ?>" target="_blank">lender</a> a full overview of your background and financial position.<p>
    <h2>Is a Pre-Approval Guaranteed?</h2>
    <p>Unfortunately not.  As long as your personal circumstances don’t change, you will very rarely run into issues as the borrower; however, the property you’re purchasing can often be the problem.  Once you have found a property and requested your pre-approval be turned into a formal approval, the bank will order a valuation on the property to check it is satisfactory.  If the property doesn’t value up, or is an unsatisfactory <a href="<?php echo $config['URL'] . 'useful-information/glossary.php/#security' ?>" target="_blank">security</a>, the bank will not proceed with the loan.</p>
    <h2>How long does a Pre Approval last?</h2>
    <p>This varies from lender-to-lender, but generally a pre-approval will last 3 months.  Many <a href="<?php echo $config['URL'] . 'useful-information/glossary.php/#lender' ?>" target="_blank">lenders</a> will also be happy extend this time period on request.</P>
    <h2>Will I need to provide payslips ETC. for a pre approval?</h2>
    <p>In order to get a pre-approval, you will have to give the lender all the required documents: Driver Licences, payslips, bank statements etc.  Once you have entered a contract, you may need to provide everything again if the payslips and bank statements are out dated (more than 30 days old), just to prove your situation has not changed.<p/>
    <h2>What are the costs involved with a pre approval?</h2>
    <p>With the vast majority of lenders, there are no costs.   A pre-approval is a free service provided by many lenders in order to help them secure your business.</p>
    <h2>What is the general turnaround time for getting a pre approval? </h2>
    <p>Depending on the time of year, and the lender of choice, it can be anywhere between 1 and 8 business days generally.  Talk to a <a href='../../../'>Mortgage Broker in Brisbane</a> for more details.</p>

