    <h2>Can I put in less than a 5% deposit as a first time buyer?</h2>
    <p>
        The only option without using a 5% deposit is if you have a parent that will go guarantor on your loan application.  For more information on how this is possible, see <a href="./how-a-guarantor-works.php">how a guarantor works</a>.
    </p>

    <h2>Is there a benefit to putting in more than a 5% deposit?</h2>
    <p>Absolutely.  If you have the option, a higher deposit will increase your chance of approval, and minimize <a href="./what-is-lenders-mortgage-insurance-in-australia.php">Lenders Mortgage insurance</a>.  Having only a 5% deposit also limits your choice of lenders significantly; having an 8% or 10% deposit will open up your options and often allow you access to cheaper home loan products.</p>
    <h2>Can I use a gift as my 5% deposit.</h2>
    <p>
        Generally, yes.  As a first time buyer, a gift as a home loan deposit is fine in most cases.  If you're using a gift and have rented through a licenced Real Estate Agent for 12 months, you should be able to a competitive interest rate.  If you are using a gift and have not been renting through a Real Estate Agent, there are still options, however they are generally much more expensive.
    </p>
