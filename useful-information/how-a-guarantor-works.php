    <h1>How a Guarantor Works</h1>
    <p>
        There is a lot of misunderstanding around how a guarantor works, however guarantor loans are being used more than ever.  According to <a rel="nofollow" href="http://www.finder.com.au">Finder.com.au</a>, approximately 33% of all first home buyers in Queensland use a gift or guarantee from their parents when buying.  Guarantor loans are becoming increasingly popular, giving parents the ability to help their children into their home by using their <a href="<?php echo $config['URL'] . 'useful-information/glossary.php/#equity' ?>" target="_blank">equity</a>, not their cash.  They do this by putting a <u>guarantee</u> on the first home buyer's loan by allowing the bank to put a "second mortgage" over their own home.  If you are looking to get a guarantor loan, always speak to a <a href='../../../mortgage-broker-brisbane'>Mortgage Broker</a> about your options first.
    </p>
    <p>
        The standard setup for a security guarantor loan consists of two loans (thus reducing the parents' exposure), as show below:
    </p>
