    <h2>What is needed for a guarantor loan?</h2>
    <p>
        From the guarantor: all that is needed is equity in their own house to cover 20% of the purchase price without overexposing themselves.
    </p>
    <p>
        The first home buyer will have to undergo a full assessment, but the main criteria are:
    </p>
    <ul>
 	<li>Australian citizenship or permanent residency.</li>
 	<li>Regular income i.e.:
        <ul>
                <li>Full-time or Part-time employment.</li>
                <li>Casual employment for at least 3 months.</li>
                <li>Self-employed for at least 12 months.</li>
        </ul>
        </li>
    </ul>
    <h2>Can you pay off the parents’ portion of the guarantor loan first?</h2>
    <p>
        Yes.  Whilst both loan accounts must be paid from the day of settlement, we can setup “loan 1” as interest-only, reducing those repayments so the extra cash can be used to pay down the principle of “loan 2” as quickly as possible.
    </p>
    <h2>Is the Guarantor at Risk?</h2>
    <p>
        The short answer is yes.  If the borrower <a href="<?php echo $config['URL'] . 'useful-information/glossary.php/#default' ?>" target="_blank">defaults</a> on their repayments, the Bank is entitled to repossess their property in order to repay the two loan accounts.  If both loans are not fully repaid after the sale of the home buyer’s property, it is the parents’ responsibility to pay out the remainder (limited to their original liability - usually 20%).
    </p>    
