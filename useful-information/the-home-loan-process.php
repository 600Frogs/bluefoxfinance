    <h1>The Home Loan Process Explained</h1>
    <p>
        Getting a home loan can be very confusing, especially if it is your first time, and as such, I have written this article to break the process down into many simple steps.  Whilst the process does vary from lender to lender, it is generally very similar, and looks a little like this:
    </p>
    <img src ="<?php echo $config['media'] . 'articles/Home-Loan-Process.png'?>" alt="The Home Loan Process Explained">
    <h2>Starting the home Loan process</h2>
    <h3>Initial Interview with Mortgage Broker or Banker</h3>
    <p>
        This is exactly what it sounds like – to kick the process off you will need to make an appointment to see a mortgage broker or banker to go through your personal circumstances and check on your borrowing capabilities.  This should take roughly 30 – 60 minutes.
    </p>
    <h3>Ask your broker about the home loan process</h3>
    <p>Ensure you understand what is happening.  Ask for estimates on time, how much everything will cost, how he will help, and any other questions you have.  No question is considered silly, so be sure you completely understand what's happening!</p>
    <h2>Getting documents together for the home loan</h2>
    <p>Once you have decided to proceed with the home loan process, you will need to supply various documents to your mortgage broker, and sign privacy declarations and application forms.  Common documents required are Driver Licences, payslips, savings statements and statements on Credit Cards and Loans.  This can all be sorted at the initial interview if you have everything prepared and ready to go.</p>
    <h2>Application Lodgement</h2>
    <p>
        It is at this point that your mortgage broker or banker will prepare and lodge a loan application for you, and send all your required documents in to the lender.  This can vary, but generally it shouldn’t take more than 24 hours from receiving all necessary documents, to lodging your application.
    </p>
    <h2>Conditional Approval</h2>
    <p>
        At this stage, the lender has almost approved your home loan, but has a couple of conditions that must be met prior to finalising your loan.  These conditions can be almost anything, but the most common ones are things like:
    </p>
    <ul>
        <li>
            Subject to a satisfactory valuation.
        </li>
        <li>
            Subject to confirming financial or employment information.  I.e. an employment check.
        </li>
        <li>
            Subject to approval by Lender’s Mortgage Insurance.  This means the lender has approved your loan application, but they now need it to be approved by LMI.
        </li>
    </ul>
    <h2>Valuation</h2>
    <p>
        The next step in the home loan process is for the lender to order a valuation on the property to check it is worth what you are paying.  If the valuation “comes back at contract price”, and there are no overly adverse or unusual comments on the report, the lender should be happy to proceed.  It will usually take approximately 24 – 72 hours to get the valuation report back.
    </p>
    <p>
        <em>Please note:  Some lenders will allow us to order and complete valuations prior to your loan being submitted.  This can save both time and effort if you time poor or unsure as to the value of the property.  Please ask me directly for more information on this.</em>
    </p>
    <h2>
        Formal Approval
    </h2>
    <p>
        At this point, the lender will issue a ‘Letter of Formal Approval’, which confirms, in writing, their commitment to provide you the loan on the date of settlement.  With this letter, you can confidently make your contract unconditional, with a little help from your conveyancer.  Your formal approval will often come within 24 hours of the valuation being done (and all other conditions being met).
    </p>
    <h2>Document Signing</h2>
    <p>After your loan has been formally approved, the lender will send out your loan documents for signing - these will often be sent to your Mortgage Broker for perusal first.  These should be signed ASAP to prevent any delays with settlement.</p>
    <h2>Settlement - The Home Loan Process is complete!</h2>
    <p>
        The home loan process is complete and you've successfully bought your first house!
    </p>
