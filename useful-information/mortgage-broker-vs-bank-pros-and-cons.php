    <h1>Mortgage Broker vs Bank - Pros and Cons</h1>
    <p><strong>Mortgage Broker vs Bank</strong>, which one should you use and why?  Whilst both a <a href='../../../'>Mortgage Broker</a> and a Bank have similar roles, (as explained <a href="<?php echo $config['URL'] . 'useful-information/what-does-a-mortgage-broker-do.php' ?>">here</a>) I have listed the pros and cons of each below, so you can make the informed decision yourself.</p>

    <h2><strong>Pros: Mortgage Broker</strong></h2>
    <ul>
            <li>Access to multiple banks and products, presumably meaning you will get a bank and product more suited to you.</li>
            <li>Although some Mortgage Brokers will charge you for their service, most will not. You can always check this prior to your initial appointment.</li>
            <li>Many Mortgage Brokers will come to your house or do the process online, making it easy and convenient for you.</li>
            <li>Many Mortgage Brokers will work after hours.</li>
            <li>Mortgage Brokers rely heavily on referrals. Providing a great experience for the customer is essential to the survival of their business.</li>
            <li>Industry body’s compliance requires mortgage brokers to disclose the full details of the home loan, including all fees.</li>
            <li>A mortgage broker can shop around for you to get you a better rate and lower fees.</li>
            <li>Many Mortgage Brokers have their own Professional Indemnity insurance, should anything go awry.</li>
    </ul>
    <h2><strong>Cons: Mortgage Broker</strong></h2>
    <ul>
            <li>They may not be who they say they are. If there is any doubt, always check they have the appropriate licencing <a href="https://connectonline.asic.gov.au/RegistrySearch/faces/landing/ProfessionalRegisters.jspx?_adf.ctrl-state=1dn3jrbh6w_23" target="_blank"><strong>here</strong></a>.  Also be sure to check their reviews, and take heed of what you have heard about them.</li>
            <li>They may charge a fee for service – check prior to proceeding.</li>
            <li>A mortgage broker may not have your best interests at heart.  This sentiment has come around because a small number of brokers have chosen to put their commission ahead of their customer.  Always check what they get paid for writing your loan, and if you are unsure how this compares to most banks, please check <a href="<?php echo $config['URL'] . 'useful-information/how-does-a-mortgage-broker-get-paid.php' ?>">this</a> article.</li>
    </ul>
    <h2><strong>Pros: Bank</strong></h2>
    <ul>
            <li>Many banks have an online or mobile banking sector, for extra convenience.</li>
            <li>You can do the shopping around yourself to decide which bank and product you want.</li>
    <li>Banks have branches</li>
            <li>Banks have been around much longer than Mortgage Brokers – this can often make customers feel more comfortable approaching a bank.</li>
    </ul>
    <h2><strong>Cons: Bank</strong></h2>
    <ul>
            <li>The banker only has access to their own bank's products.</li>
            <li>If your situation doesn’t fit the banks policy, you will be stuck. For example, Westpac requires casual employees to be in their job for 12 months minimum, but CommBank will take you after only 3 months’ employment – little things like this can often be the difference between being approved and declined.</li>
            <li>Bankers work bank hours.</li>
    </ul>
