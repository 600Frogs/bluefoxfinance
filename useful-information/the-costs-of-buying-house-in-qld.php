    <h2>Total costs of Buying a House in QLD - Conclusion</h2>
    <p>
          Based on the total of all the above figures, the likely cost of purchasing an owner-occupied house valued at $400,000 in Queensland is approximately $24,953 for a first home buyer or $30,203 otherwise.  Keep in mind that for a first home buyer eligible for the $20,000 Queensland Great Start Grant: this $20,000 can sometimes be used to cover upfront costs and bank deposits on a brand new home (not applicable for investment or construction).  
