    <h1>The Costs of Buying a House in QLD</h1>
    <p>
        There’s a few different opinions on the costs of buying a house in Queensland, so I’ve put together this article to give you an accurate breakdown of the various costs. I’ve based all relevant costs on a standard house valued at $400,000.  It is however important to remember that everyone's situation is different, and you are always best to speak to a <a href='../../../'>Mortgage Broker in Brisbane</a> for a free proposal on expected costs.
    </p>
    
    <h2>Conveyancing Costs when buying a house in QLD</h2>
    <p>
       It is possible to do your own conveyancing; but, is highly recommended you hire a professional to do it for you and guide you through the process.  The costs associated can be broken up as follows:
    </p>
    <ul>
        <li>
            Professional costs: This varies from solicitor to solicitor, but $880 is a fair estimate, based on my own property purchase.
        </li>
        <li>
            Search fees: This covers costs associated with various searches on the property, and will change depending on which searches you opt to do. I have asked a local solicitor who has advised $800 is a reasonable estimate.
        </li>
        <li>
            Settlement Notice: To lodge a “Settlement Notice” with the QLD Government currently carries a $33 charge.
        </li>
    </ul>
    <p>
       For a total cost estimate of $1713. 
    </p>
    <h2>Government Charges</h2>
    <p>
        When purchasing your home, there are a couple of government charges applicable, the first of which is the Stamp Duty.  The best option for getting an accurate idea of how much this will cost would be to use the stamp duty estimator, which can be found <a rel='nofollow' href='https://www.afgonline.com.au/get-a-loan/home-loan/online-calculators/stamp-duty-calculator/'>here</a>.  As an idea, purchasing an existing home at $400,000 is likely to set you back $5,250 in stamp duty; unless, you are a first home buyer, in which case you pay $0 stamp duty up to a purchase price of $500,000 - this offer has recently been extended until 30th June 2018.
    </p>
    <p>
       The second government chargeis the Transfer Fee, which is payable regardless of your 'first home buyer' status.  For a $400,000 house, the transfer fee is $929.  You can find an estimate for other prices <a rel='nofollow' href='https://www.afgonline.com.au/get-a-loan/home-loan/online-calculators/stamp-duty-calculator/'>here</a>.
    </p>
    <p>
       The final government charge, which is often classified as a bank fee, is a “Mortgage Registration Fee”.  This is payable at settlement so the lending institution can register their mortgage over the property – it is currently set at $181 in Queensland.
    </p>
    <h2>Other Costs when buying a home</h2>
    <p>
        At settlement, you will need to pay a rates adjustment to cover Council rates, Body Corporate and other costs; which you, as the buyer, are responsible for paying from the day following settlement.  This amount can vary greatly depending on the property and time of year, but should be kept in mind when purchasing a house – your conveyancer can advise you how much this will be once they have completed the council search.  As an idea, I paid $1,430 when purchasing my own unit in March 2016, $752 being a rates adjustment and the balance being for Body Corporate fees.
    </p>
    <p>
        Building and pest inspections are not compulsory, but are highly recommended in most cases – I will usually budget in $700 for this.
    </p>
    <p>
        <em>Please note: moving costs and insurances are beyond the scope of this article.</em>
    </p>
    <h2>Home Loan Deposit</h2>
    <p>
        Currently in Brisbane all you usually need is a 5% deposit to purchase a house, however please refer to <a href='../../../useful-information/how-much-deposit-do-i-need-to-buy-a-house-in-qld'>how much deposit do I need to buy a house in Qld</a> for eligibility and details (and why you might want a bigger deposit).  Based on a $400,000 house, this would equate to $20,000.  There are currently loan products available with no upfront or ongoing fees, but whether there are any fees applicable will, of course, depend on your personal circumstances.  When using a deposit below 20%, banks will require that you pay Lenders Mortgage Insurance (LMI); however, this charge can be added into the loan and is not needed to be paid upfront.
    </p>
