    <h1>Cross Collateralization Explained</h1>
    <p>
        <strong>Cross collateralization</strong> is the process of using <a href="<?php echo $config['URL'] . 'useful-information/glossary.php/#collateral' ?>" target="_blank">collateral</a> from one loan as the <a href="<?php echo $config['URL'] . 'useful-information/glossary.php/#Security' ?>" target="_blank">security</a> for an additional loan(s).   There are very few benefits of cross collateralization for the customer, and it's often a misused substitute for multiple standalone loans due to lack-of-understanding.  Always speak to a <a href='../../../'>Mortgage Broker</a> about your individual situation.
    </p>
    <p>
        Cross collateralization can be used almost any time two properties are involved and the equity in one is required to refinance/purchase a second or third property.  A common situation where cross collateralization occurs is when a home owner wants to use <a href="<?php echo $config['URL'] . 'useful-information/glossary.php/#equity' ?>" target="_blank">equity</a> in their owner-occupied house to purchase an investment property.  Using cross collateralization, the loan setup for this scenario will look like this:
