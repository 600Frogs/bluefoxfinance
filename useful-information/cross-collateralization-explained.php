    <h2>What's wrong with Cross Collateralization?</h2>
    <p>
        The above setup can have no adverse effects or complications, but what happens when you want to sell or refinance one of your properties?  When this happens, the bank will usually require valuations on <u>both</u> properties (usually at the customer’s expense), and a significant drop in the value of one property can prevent the sale of the other.  Because both properties are tied together, the bank has the right to ensure the <u>overall <strong><a href="<?php echo $config['URL'] . 'useful-information/glossary.php/#lvr' ?>" target="_blank">Loan to Value Ratio (LVR)</a></strong></u><strong> </strong>stays below 90%, which means the total loan amount cannot exceed 90% of the combined property values.
    </p>
    <p>
        Before the bank will discharge either mortgage, if the post-sale LVR is higher than the pre-sale (when the properties were cross collateralized) LVR, the bank will usually want to re-asses your situation and risk factors, meaning you might have to provide financial and personal documents to prove your situation is stable.
    </p>
    <p>
        If the sale or refinance is approved by the bank, but the post-sale LVR is above 80%, the bank will require the borrower to pay an <a href="<?php echo $config['URL'] . 'useful-information/glossary.php/#lmi' ?>" target="_blank">LMI</a> (Lender's Mortgage Insurance) premium.  This can amount to tens of thousands of dollars in some cases.
    </p>
    <h2>What are the Advantages of Cross Collateralization?</h2>
    <p>
        The only benefits of cross collateralization for the customer are:
        <ol>
            <li>Ease of transaction: the loan setup can be completed in one simple transaction.</li>
            <li>Loan establishment fees: these can be minimized through the process.</li>
        </ol>
    </p>
    <h2>Should I Cross Collateralize?</h2>
    <p>
        In the majority of cases, the answer is no.  The potential additional fees and complications are seldom worth the benefits.  Cross collateralization can be a worthwhile approach if the LVR is very low, or if you can be certain they will not be selling or refinancing either property (or using them to guarantee other loans) in the short-to-medium term.  Even when confidence is high, a drop in value of either properties can make your situation far more complicated than it should have been.
    </p>
    <h2>So why do people do it?</h2>
    <p>
        It’s much simpler to setup a loan this way, as it saves time writing, submitting, assessing, and signing multiple loan applications.  It is also often easier for a borrower to understand, when compared to the alternative.  Certain banks are also known to encourage cross collateralization as it lowers the bank's exposure to risk.
    </p>
    <h2>How to avoid Cross Collateralization?</h2>
    <p>
        The alternate method is setting up multiple <a href="<?php echo $config['URL'] . 'useful-information/glossary.php/#standaloneloan' ?>" target="_blank">stand-alone loans</a>, meaning only one security is used per loan account.  The same example from before might look like this, when set up using stand-alone loans:
