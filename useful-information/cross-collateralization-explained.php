    <p>
        <em>Note: The owner-occupied property is security for one loan, with two <a href="<?php echo $config['URL'] . 'useful-information/glossary.php/#split' ?>" target="_blank">splits</a>, one of which must be investment.  The loan type must legally be defined by the purpose of the loan, not the security.  Because there is $100,000 equity in the owner-occupied property to purchase the investment property, it must be setup as an investment split.  This is also the reason we need to setup 3 loan accounts/splits.</em>
    </p>
    <p>
        As you can see here, the overall loan amount, LVR, and security value are the same as in the first scenario, yet the loans are setup completely different.  The major difference is that no loan is secured by multiple properties, meaning you can sell and refinance each property independently without the bank requesting additional valuations and assessments.
    </p>
    <h2>How to get out of Cross Collateralization?</h2>
    <p>
