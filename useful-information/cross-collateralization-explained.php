    <h2>Cross Collateralization and Lender’s Mortgage Insurance (LMI)</h2>
    <p>
        Another point to consider, is that setting up a loan above 80% <a href="<?php echo $config['URL'] . 'useful-information/glossary.php/#lvr' ?>" target="_blank">LVR</a> (less than 20% deposit/equity) generally requires the borrower to pay Lender's Mortgage Insurance, which is usually more expensive when properties are cross collateralized.  When cross collateralized, the Mortgage Insurer will assess the risk by looking at the overall LVR and the total security value (i.e. 'their exposure') i.e. $900,000 – which can make very large LMI premiums.  By using stand-alone loans, the LVR of one loan can be manipulated to 80%, whilst increasing the LVR of the other: the Mortgage Insurer is now only exposed to one security/loan worth $500,000, making the premium much lower (despite the higher LVR).  Unfortunately many consumers and professionals are unaware of this, and the customer will, in some cases, pay $5,000+ more than they needed to on their LMI premium.
    </p>
    <h3>In Summary</h3>
    <p>
        It is possible for cross collateralization to be beneficial for the borrower, but in most cases it is not worth the risk.  When setting up your loans, please pay close attention.  It is always recommended you speak to a professional about your situation, but also ensure you understand the reason behind their recommendations.  Be sure to ask questions about anything you don't understand, and don't hesitate to get a second opinion if anything seems amiss.
