    <h1>LMI: What is Lenders Mortgage Insurance in Australia?</h1>
    <p>Lenders Mortgage Insurance (LMI) in Australia is a type of insurance that allows consumers to purchase a home with less than 20% deposit.  All major banks throughout Australia currently require you to pay LMI if you have less than a 20% deposit and have no guarantor.  LMI is paid as a once off premium at the start of your loan, and can be paid up front, or added into the loan.</p>
    <p>Before the Bank gives you a formal approval on your loan application, they will submit your LMI application to the insurer; failure to get approved for LMI will unfortunately result in your whole application being declined.  LMI is used to protect the Bank in case you default on your loan, allowing them to make a claim against any losses.</p>
    <h2>Do I need to pay Lenders Mortgage Insurance?</h2>
    <p>If you don’t have a 20% deposit, you will always be required to pay LMI with any traditional bank.  Some non-banks don’t require LMI approval; however, will charge a fee of their own which is generally more expensive – this can; however, be a great alternative for people that don’t meet the insurers eligibility criteria.</p>
    <h2>Does Lenders Mortgage Insurance Protect me?</h2>
    <p>No.  Lender’s Mortgage Insurance is purely for the benefit of the bank and will not protect you in any way should you not be able to make your repayments.  For personal protection, please look into Mortgage Protection Insurance, Income Protection and Life Insurance.</p>
    <h2>How much does LMI cost?</h2>
    <p>You can find a premium estimator <a href="https://www.genworth.com.au/lender/lmi-tools/lmi-premium-estimator/" target="_blank">here</a>; however you should always talk to a <a href= '../../../'>Mortgage Broker</a> for a more accurate figure.   It is also important to remember this needn’t (and in most cases, shouldn’t), be paid up front, but rather added into your loan (It actually works out cheaper this way).</p>
    <h2>Who Provides Lenders Mortgage Insurance in Australia?</h2>
    <p>There are two major LMI providers in Australia: Genworth and QBE; with a 3rd, international insurer being used by some lenders – Arch.</p>
    <h2>How will I know if I’m eligible for LMI?</h2>
    <p>Mortgage Insurers policy documents are hundreds of pages long and unavailable to the general public.  You will need to speak to a Mortgage Broker to find out if you are eligible for approval.</p>
    <h2>What if I’m not approved for Mortgage Insurance?</h2>
    <p>There are still options.  There are other financial institutions throughout Australia that will lend to people with a low deposit in a wide variety of circumstances – generally at a higher interest rate.  Talk to us for more information.</p>

