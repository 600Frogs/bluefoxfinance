    <h1>First Time Home Buyers: How to Buy a House?</h1>
    <h2>Where Should You Start?</h2>
    <p>Figuring out how to buy a house as a first time home buyer can seem overwhelming and confusing, but by talking to the right people, and understanding both the processes and Government incentives available, everything will become much simpler.</p>
    <p>Generally, a first home buyer’s first step is to ask questions: ask for your parents and friends’ advice first, as they may be able to explain much of the process for you.  You will also need to talk to various professionals:</p>
    <ul>
        <li>
            A <b>local Real Estate Agent</b> – Have a look at some affordable houses, talk to the agents and gather some information about the local market. Real Estate Agents deal with first home buyers all the time and should be able to answer your questions about <a href="<?php echo $config['URL'] . 'useful-information/the-home-buying-process.php' ?>" target="_blank">the home buying process</a> and the concession/Grant available to first home buyers.
        </li>
        <li>
            A <a href='../../../'><b>Mortgage Broker</b></a> – A Mortgage Broker will work with you and the banks to arrange your first home loan, usually at no cost to you (for more information see <a href="<?php echo $config['URL'] . 'useful-information/how-does-a-mortgage-broker-get-paid.php' ?>" target="_blank">How Does A Mortgage Broker Get Paid</a>). Before you can consider making an offer on a house, it is really important you talk to a <a href="<?php echo $config['URL'] ?>" target="_blank">Mortgage Broker</a> to find out if you qualify for a loan, and your borrowing capacity limit. They will be able to tell you <a href="<?php echo $config['URL'] . 'useful-information/the-costs-of-buying-house-in-qld.php' ?>" target="_blank">how much money you need to buy a house</a>, and look at other options such as parents applying to be <a href="<?php echo $config['URL'] . 'useful-information/how-a-guarantor-works.php' ?>" target="_blank">security guarantors</a>.  Mortgage Brokers assist first home buyers regularly and are accustomed to walking them through the entire home buying process.
        </li>
        <li>
            A <b>Conveyancer</b> – A conveyancer is a solicitor or representative responsible for preparing legal documents and arranging settlement.  Whilst it is possible you do your own conveyancing, it is highly recommended you hire a professional.  Whilst often polite, it is not actually necessary to talk to a solicitor before signing a contract.  Ask your family, Real Estate Agent or Mortgage Broker for a recommendation.
        </li>
    </ul>
    <h2>
        How much money will you need?
    </h2>
    <p>
        As a rough estimate, a first home buyer will <u>usually</u> need about 5% of the purchase price, plus approximately $3,000 more for Solicitor and Government charges.  A full breakdown of the costs involved can be found at: <a href="<?php echo $config['URL'] . 'useful-information/the-costs-of-buying-house-in-qld.php' ?>" target="_blank">The costs of buying a house in QLD</a>.  In some cases, a larger deposit will be required, for reasons such as:
    </p>
    <ul>
        <li>
            Poor credit history – Having defaults or other adverse conduct on your credit file.  If you think this may be an issue, you can order your credit file <a href="https://www.equifax.com.au/personal/products/my-credit-file" rel="nofollow">here</a>, or talk to your Mortgage Broker.
        </li>
        <li>
            Unsuitable security – For example: you will need a 20% deposit if you choose to buy a property on 100 hectares.
        </li>
        <li>
            Postcode restrictions – many lenders have ‘postcode restrictions’ in various regional/mining towns that can result in a larger deposit being required.  Brisbane seldom has such restrictions.
        </li>
    </ul>
    <p>
        Your Mortgage Broker will be able to asses your individual situation and advise you of any potential problems.
    </p>
    <h3>
        First Home Owners' Grant
    </h3>
    <p>
        Each state in Australia has different requirements for the First Home Owners’ Grant, and differing Grant amounts.  The following information is relevant for Queensland, however please click <a href="http://www.firsthome.gov.au/" target="_blank">here</a> for information in other states.
    </p>
    <p>
        >Up until 30<sup>th</sup> June 2018, the Queensland Government is offering first home buyers $20,000 cash at settlement for individuals or couples buying brand new homes.  To be eligible for the grant:
    </p>
    <ul>
 	<li>You must be an Australian Citizen or permanent resident (or applying with someone who is).</li>
 	<li>You or your spouse must not have previously owned property in Australia.</li>
 	<li>You must be at least 18 years of age.</li>
 	<li>You must be buying or building a brand new home, valued under $750,000.</li>
    </ul>
    <p>
        The term 'new home' is defined as a house, apartment, unit or townhouse that:
    </p>
    <ul>
        <li>Has not been previously occupied as a place of residence, and has not been previously sold as a place of residence; or</li>
 	<li>Is a <a href="https://firsthomeowners.initiatives.qld.gov.au/definitions.php#substantially-renovated-homes">substantially renovated home</a>.</li>
    </ul>
    <p>For more information on the Queensland First Home Owners’ Grant, visit their website:  <a href="https://firsthomeowners.initiatives.qld.gov.au">https://firsthomeowners.initiatives.qld.gov.au</a>.</p>
    <h3>First Home Buyers' Concession</h3>
    <p>
        If you are buying your first home, be it new or old, you are eligible for a stamp duty concession.  The concession is applicable up to a purchase price of $550,000, but its benefits lessen drastically after $500,000.
    </p>
    <p>
        If you are purchasing your first property under $500,000, the concession should cover the entire cost of stamp duty.  You can find stamp duty and concession/Grant calculators <a href="https://www.qld.gov.au/housing/buying-owning-home/calculating-transfer-duty">here</a>, and more information on the First Home Concession <a href="https://www.qld.gov.au/housing/buying-owning-home/first-home-concession/">here</a>.
    </p>
    <p>
        Please note that the First Home Owners’ Grant and First Home Concession are separate incentives and can be used in conjunction.
    </p>
