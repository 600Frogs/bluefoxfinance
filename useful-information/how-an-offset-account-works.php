    <h1>How an offset account work?</h1>
    <p>Instead of earning interest on money in a savings account, it could be in your best interest to put your savings into an offset account.  Here’s a simple example showing how an offset account works:
    </p>
    <ul>
            <li>John has a home loan of $200,000 at 4.20%.</li>
            <li>John also has a 100% offset account linked to his home loan which currently has $40,000 in it.</li>
            <li>Interest only accrues on $160,000 ($200,000 - $40,000) of John’s home loan.</li>
    </ul>
    <p>This is beneficial for a few reasons -</p>
    <ol>
            <li>John is saving money at a rate of 4.20% p.a. where he might only get 3.00% in a savings account.</li>
            <li>Because of the reduced interest payable on the loan, John will pay the loan out in only 23 years 6 months instead of 30 years (Saving approx. $37,000 in interest payments!).</li>
            <li>John would usually have to pay tax at his personal income bracket on any interest earned from a savings account; however, he won’t pay any tax on the money he saved.</li>
            <li>If John needs his $40,000 for any reason, it is easily accessible to him at no cost.</li>
    </ol>
    &nbsp;
    <h2><strong>So how do I make the most of an offset account?</strong></h2>
    <p>Scarcely known is the fact that banks calculate interest on a daily basis, but generally only charge it to the account on a monthly basis; it is therefore important to maximize the money you keep in your offset account on a daily basis.
        </p>
    <p>The most simple way to utilise your offset account is by having your salary paid into that account, and held there as long as possible.  The most effective way to do this is by using a credit card for day-to-day bills and transactions, then paying it off every month using your offset account – this will keep as much money as possible in your offset account for the month, potentially saving thousands over the life of the loan.
        </p>
    <p>Luckily, a lot of lenders offer what is called a “professional package” which includes a 100% offset account and a fee-free credit card for an annual ‘package fee’.
        </p>
    &nbsp;
    <h2><strong>Should I get an offset account?</strong></h2>
    <p>Offset accounts shave time and interest payments off your loan when used properly, however they usually attract additional fees and/or increased interest rates. <a href="http://www.visionabacus.com/Finance/Australia/1/SuiteA100/640/Home-Loan-Offset-Calculator.aspx" target="_blank">This calculator</a> can show you how much an offset account will save you.  Offset accounts are not always the best option for everyone, it is best to talk to your Mortgage Broker for more information on fees/rates involved.
        </p>
