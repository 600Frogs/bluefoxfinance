    <h2>How will contributing a larger home loan deposit benefit me?</h2>
    <p>
        Using the minimum allowable deposit is a great option for many people to get into the housing market without saving a large deposit; however, if you have more funds available, there are distinct advantages for using a larger home loan deposit:
    </p>
    <ul>
        <li><strong>0% Deposit</strong>: Can only be done using equity in a guarantor's property.  See <a href='../../../useful-information/how-a-guarantor-works'>how a guarantor works</a> for more information.</li>
        <li><strong>5% Deposit</strong>: The absolute minimum cash deposit required to buy a house in QLD.</li>
        <li><strong>8% Deposit</strong>: To account for the <a href='../../../useful-information/what-is-lenders-mortgage-insurance-in-australia'>Lenders Mortgage Insurance</a> Premium, most major and second tier banks will require approximately 8% as a deposit.  Using an 8% deposit will generally get you a much cheaper interest rate than at 5%.</li>
        <li><strong>10% Deposit</strong>: Some lenders will not lend above 90% of the value of the property (90% LVR).  Having a 10% deposit will open these lenders up to you and could mean you get a better deal.  Lender's Mortgage Insurance Premiums are also much less with a 10% home loan deposit.</li>
        <li><strong>15% Deposit</strong>: Many banks will accept "non-genuine savings" at 85% LVR, so this is often a good figure if you are using a gift from your parents as a deposit.</li>
        <li><strong>20% Deposit</strong>: The magic number.  Whilst some banks will reduce interest rates at lower LVRs, having a 20% deposit will usually get you the best deal out there.  You also will not need to pay a Lender's Mortgage Insurance Premium, and will be subject to less strict approval criteria in general.</li>
    </ul>
